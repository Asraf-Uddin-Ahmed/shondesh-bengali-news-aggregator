    void delete(String destPath){
        path = destPath;
        this.setTitle("Delete");
        this.setVisible(true);
        
        FileDirectory fd = new FileDirectory();
        int maxFile = fd.getDirectoryChildNumber(new File(destPath))/2;
        String realFileNames[] = new String[maxFile];
        myFileNames = new int[maxFile];
        
        int countFile = 0;
        for(int I=0; countFile<maxFile; I++){
            String myFileName = destPath + "\\" + String.valueOf(I) + ".head";
            try{
                realFileNames[countFile] = fd.getFileString(myFileName);
                myFileNames[countFile] = I;
                countFile++;
            }catch(IOException ioe){}
        }
        jList1.setListData(realFileNames);
    }
    
