    void add(String destPath){
        JFileChooser jfc = new JFileChooser();
        jfc.setApproveButtonText("Add");
        jfc.setDialogTitle("Add (less than 50KB)");
        
        //file add button clicked.
        int ret = jfc.showDialog(jfc, null);
        if (ret == JFileChooser.APPROVE_OPTION) {
            //checking fileSize
            long fileSize = jfc.getSelectedFile().length(); //fileSize in Bytes
            System.out.println("= "+fileSize);
            if( fileSize > (1024*50) ){   //measuring in 50KB
                JOptionPane.showMessageDialog(rootPane, "File size is huge.\nPlease choose a file less than 50KB.", "Error", JOptionPane.ERROR_MESSAGE);
                add(destPath);
                this.dispose();
                return ;
            }
            
            try {
                FileDirectory fd = new FileDirectory();
                String text = fd.getFileString(jfc.getSelectedFile().getPath());
                //System.out.println(jfc.getSelectedFile().getName()+" -> "+text);
                fd.makeDirs(new File(destPath));
                int I = 0;
                String filePath = destPath + "\\" + String.valueOf(I);
                for( ; fd.isExist(new File(filePath+".head"))==true; I++){
                    System.out.println("=> "+filePath);
                    filePath = destPath + "\\" + String.valueOf(I);
                }
                fd.makeFileUtf(filePath+".head", jfc.getSelectedFile().getName());    //file name is saved as headline
                fd.makeFileUtf(filePath+".news", text);                               //file contents are saved as news
                
                //again call
                add(destPath);
            }
            catch (Exception ex) {
                //Logger.getLogger(SearchPage.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "File doesn't exist.\nPlease select a file.", "File not fuond !!", JOptionPane.ERROR_MESSAGE);
            }
        }
        this.dispose();
    }
    
